    public void mostrarGatos() {
        int f = 0;
        List<Animal> lista = obj.getListaAnimales();
        DefaultTableModel modelo = (DefaultTableModel) tablaGatosMedicos.getModel();
        modelo.setColumnCount(6); // Nro, ID, Nombre, Edad, Estado Salud, Disponible
        modelo.setRowCount(0);

        if (lista != null && !lista.isEmpty()) {
            for (Animal a : lista) {
                if (a instanceof Gato) { // FILTRO PARA GATOS
                    modelo.setRowCount(f + 1);
                    Gato g = (Gato) a;
                    tablaGatosMedicos.setValueAt(f + 1, f, 0);
                    tablaGatosMedicos.setValueAt(g.getId(), f, 1);
                    tablaGatosMedicos.setValueAt(g.getNombre(), f, 2);
                    tablaGatosMedicos.setValueAt(g.getEdad(), f, 3);
                    tablaGatosMedicos.setValueAt(g.getEstadoSalud(), f, 4);
                    tablaGatosMedicos.setValueAt(g.isDisponible() ? "Sí" : "No", f, 5);
                    f++;
                }
            }
        }
    }

