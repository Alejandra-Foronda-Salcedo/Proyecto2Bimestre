    private void btnRegistrarRevisionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarRevisionActionPerformed
        String idDigitado = txtIdBuscar.getText().trim();
    String nuevoEstado = cmbNuevoEstado.getSelectedItem().toString();
    
        
    Animal animal = obj.buscarAnimalPorId(idDigitado);
    
    // VALIDAMOS QUE SEA UN GATO
    if (animal == null || !(animal instanceof Gato)) {
        msjLbl.setText("EL ID DIGITADO NO CORRESPONDE A NINGÚN GATO REGISTRADO");
        msjLbl.setForeground(java.awt.Color.red);
        return;
    }
    
    boolean exito = obj.procesarRevisionMedica(idDigitado, nuevoEstado);
    
    if (exito) {
        String mensaje = "Revisión registrada para el gato: " + animal.getNombre() + "\nSalud: " + nuevoEstado;
        if (nuevoEstado.equalsIgnoreCase("Saludable")) {
            mensaje += "\n¡El gato ahora vuelve a estar DISPONIBLE para adopción!";
        }
        
        javax.swing.JOptionPane.showMessageDialog(this, mensaje);
        
        txtIdBuscar.setText("");
        cmbNuevoEstado.setSelectedIndex(0);
        msjLbl.setText("");
        mostrarGatos(); 
    }
    }//GEN-LAST:event_btnRegistrarRevisionActionPerformed


