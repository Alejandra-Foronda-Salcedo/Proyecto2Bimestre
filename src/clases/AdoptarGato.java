    public void mostrarGatosDisponibles() {
        int f = 0;
        listaAnimales = obj.getListaAnimales();
        DefaultTableModel modelo = (DefaultTableModel) tablaPerrosDispo.getModel();
        modelo.setColumnCount(5); // Columnas: Nro, ID, Nombre, Edad, Raza
        modelo.setRowCount(0); // Limpiar filas anteriores

        if (listaAnimales != null && !listaAnimales.isEmpty()) {
            for (Animal animal : listaAnimales) {
                if (animal instanceof Gato && animal.isDisponible()) {
                    modelo.setRowCount(f + 1);
                    Gato g = (Gato) animal;

                    tablaPerrosDispo.setValueAt(f + 1, f, 0);
                    tablaPerrosDispo.setValueAt(g.getId(), f, 1);
                    tablaPerrosDispo.setValueAt(g.getNombre(), f, 2);
                    tablaPerrosDispo.setValueAt(g.getEdad(), f, 3);
                    f++;
                }
            }
        }

        if (f == 0) {
            msjLbl.setText("NO HAY GATOS DISPONIBLES EN ESTE MOMENTO");
            msjLbl.setForeground(java.awt.Color.red);
        } else {
            msjLbl.setText("");
        }
    }

