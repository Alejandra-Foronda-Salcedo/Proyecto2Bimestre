    private void btnAdoptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdoptarActionPerformed
        String dni = txtDniAdoptante.getText().trim();
        String idAnimal = txtIdAnimal.getText().trim();

        if (dni.isEmpty() || idAnimal.isEmpty()) {
            msjLbl.setText("POR FAVOR, INGRESE AMBOS CAMPOS (DNI E ID)");
            msjLbl.setForeground(java.awt.Color.red);
            return;
        }

        // 1. Verificar si el adoptante existe
        Adoptante adoptanteEncontrado = obj.buscarAdoptantePorDni(dni);

        // 2. Verificar si el animal existe
        Animal animalEncontrado = obj.buscarAnimalPorId(idAnimal);

        if (adoptanteEncontrado == null) {
            msjLbl.setText("EL DNI DEL ADOPTANTE NO ESTÁ REGISTRADO");
            msjLbl.setForeground(java.awt.Color.red);
        } else if (animalEncontrado == null || !(animalEncontrado instanceof Gato)) {
            msjLbl.setText("EL ID DEL GATO NO EXISTE O NO ES VALIDO");
            msjLbl.setForeground(java.awt.Color.red);
        } else {
            if (!animalEncontrado.isDisponible()) {
                msjLbl.setText("ESTE ANIMAL YA FUE ADOPTADO ANTERIORMENTE");
                msjLbl.setForeground(java.awt.Color.red);
                return;
            }

            adoptanteEncontrado.getAnimalesAdoptados().add(animalEncontrado);


            animalEncontrado.setDisponible(false);


            javax.swing.JOptionPane.showMessageDialog(this,
                    "¡ADOPCIÓN EXITOSA!\nEl perro " + animalEncontrado.getNombre()
                    + " ahora pertenece a la lista de " + adoptanteEncontrado.getNombre());

            // 4. Limpiar componentes y refrescar la tabla
            txtDniAdoptante.setText("");
            txtIdAnimal.setText("");
            msjLbl.setText("");
            mostrarGatosDisponibles();
        }
    }//GEN-LAST:event_btnAdoptarActionPerformed


