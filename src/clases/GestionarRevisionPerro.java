    private void btnRegistrarRevisionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarRevisionActionPerformed
        // Captura el ID digitado manualmente
        String idDigitado = txtIdBuscar.getText().trim();
        String nuevoEstado = cmbNuevoEstado.getSelectedItem().toString();

        if (idDigitado.isEmpty()) {
            msjLbl.setText("POR FAVOR, DIGITE EL ID DEL PERRO");
            msjLbl.setForeground(java.awt.Color.red);
            return;
        }

        // Buscamos primero si el animal existe y realmente es un Perro
        Animal animal = obj.buscarAnimalPorId(idDigitado);

        if (animal == null || !(animal instanceof Perro)) {
            msjLbl.setText("EL ID DIGITADO NO CORRESPONDE A NINGÚN PERRO REGISTRADO");
            msjLbl.setForeground(java.awt.Color.red);
            return;
        }

        // Procesamos la revisión médica con la nueva regla
        boolean exito = obj.procesarRevisionMedica(idDigitado, nuevoEstado);

        if (exito) {
            String mensaje = "Revisión registrada para el perro: " + animal.getNombre() + "\nSalud: " + nuevoEstado;
            if (nuevoEstado.equalsIgnoreCase("Saludable")) {
                mensaje += "\n¡El perro ahora vuelve a estar DISPONIBLE para adopción!";
            }

            javax.swing.JOptionPane.showMessageDialog(this, mensaje);

            // Limpiamos el campo digitado y refrescamos la tabla
            txtIdBuscar.setText("");
            cmbNuevoEstado.setSelectedIndex(0);
            msjLbl.setText("");
            mostrarPerros();
        }
    }//GEN-LAST:event_btnRegistrarRevisionActionPerformed


