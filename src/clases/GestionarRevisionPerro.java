    public void mostrarPerros() {
        int f = 0;
        List<Animal> lista = obj.getListaAnimales();
        DefaultTableModel modelo = (DefaultTableModel) tablaPerrosMedicos.getModel();
        modelo.setColumnCount(6); // Nro, ID, Nombre, Edad, Estado Salud, Disponible
        modelo.setRowCount(0);

        if (lista != null && !lista.isEmpty()) {
            for (Animal a : lista) {
                if (a instanceof Perro) {
                    modelo.setRowCount(f + 1);
                    Perro p = (Perro) a;
                    tablaPerrosMedicos.setValueAt(f + 1, f, 0);
                    tablaPerrosMedicos.setValueAt(p.getId(), f, 1);
                    tablaPerrosMedicos.setValueAt(p.getNombre(), f, 2);
                    tablaPerrosMedicos.setValueAt(p.getEdad(), f, 3);
                    tablaPerrosMedicos.setValueAt(p.getEstadoSalud(), f, 4);
                    tablaPerrosMedicos.setValueAt(p.isDisponible() ? "Sí" : "No", f, 5);
                    f++;
                }
            }
        }
    }

